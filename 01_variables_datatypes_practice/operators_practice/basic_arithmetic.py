a=float(input("Enter value of a: "))
b=float(input("Enter value of b: "))
sum=a+b
print(f"The sum of {a} and {b} is: {sum}")
difference=a-b
print(f"The difference when {b} is subtracted from {a} is: {difference}")
product=a*b
print(f"The product of {a} and {b} is: {product}")
quotient=a/b
print(f"The quotient when {a} is divided by {b} is: {quotient}")    
floor_division=a//b
print(f"The floor division when {a} is divided by {b} is: {floor_division}")
remainder=a%b
print(f"The remainder when {a} is divided by {b} is: {remainder}")
exponentiation=a**b
print(f"The result of {a} raised to the power {b} is: {exponentiation}")